// DATA TYPES:

// Undefined - a variable that has not been assigned a value yet
let x;
let y, z;

// String - a sequence of characters in single ('') or double ("") quotes
let name = "John Doe";
let message = "Hello, my name is " + name + ".";

// Number - numeric values, including integers and decimals
let age = 32;
let cost = 10.50;

// Boolean - true or false values
let isWorking = false;
let hasPassed = true;

// Arrays - ordered collections of values starting from index 0, used for creating lists
let fruits = ["apple", "banana", "orange"];
let numbers = [1, 2, 3, 4, 5];

// Null - a variable with a non-value assigned
let nullVar = null;

// Object - a collection of key-value pairs containing properties and (if applicable) methods
let car = 
{
    color: "Blue",
    model: "Toyota",
    year: 2021,
    size: "Large",
    maxSpeed: 120,
}

// Function - a reusable block of code that executes a specific task. They can accept parameters, return values and encapsulate code.
function greet(name)
{
    console.log("Hello, " + name + "!");
}